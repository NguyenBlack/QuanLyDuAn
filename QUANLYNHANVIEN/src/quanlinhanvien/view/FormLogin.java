    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (validates() == false) {
            validate();
        } else {
            List<Logins> list = LoginService.CheckLogin();
            int count = 0;
            for (Logins logins : list) {
                if (logins.getUser().equals(txtuser.getText()) && logins.getPass().equals(txtpass.getText())) {
                    count++;
                    break;
                }
            }
            if (count == 1) {
                JOptionPane.showMessageDialog(null, "Đăng nhập thành công");
                f.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Sai tài khoản hoặc mật khẩu");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (validates() == false) {
            validate();
        } else {
            List<Logins> list = LoginService.CheckLogin();
            int count = 0;
            for (Logins logins : list) {
                if (logins.getUser().equals(txtuser.getText())) {
                    count++;
                }
            }
            if (count == 1) {
                JOptionPane.showMessageDialog(null, "Tên tài khoản này đã có");
            } else {
                LoginService.register(txtuser.getText(), txtpass.getText());
                JOptionPane.showMessageDialog(null, "Đăng ký thành công");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

